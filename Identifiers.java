public class Identifiers {
    public static void main(String[] args){
        int x = 9;        
    }
}

//identifiers are name given to variables, class, method, packages, interfaces,
//name used to identify programming elements with unique names.
//Every java variable must be identified with unique name


//Lets identify the identifiers
//class name : Identifiers
//method name:main
//predefined class name:String
//variable name:x
//parameter name:args

// Rules to name an identifier-to avoid complilation error
//allowed characters for identifier-alpha-neumeric characters[A-Z][a-z][0-9][$][_]
//@ not allowed as it is special character
//if identifier starts with digit [0-9], It is not valid Identifier . Ex[123Geeks]
// Java identifiers are case-sensitive
// There is no limit for length of identifier but is optimal to use the length of 4-15 letters
// Reserved words cant be used as identifier . Example int while = 10 . It is invalid statement as while is a reserved word in java.
//Java has 53 reserved words . 50 keywords 3 literals. These are not allowed to be used as identifier.




